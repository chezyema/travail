    public Eleve getEleveFromFields() {
        Eleve eleve = new Eleve();
        String registreNational = registreNationalTextField.getText();
        registreNational = registreNational.replace("-", "");
        registreNational = registreNational.replace(".", "");
        //Sinon je peux setté son attribut avec la valeur récupérée.
        eleve.setId(String.valueOf(registreNational));
        eleve.setNomEleve(nomeleveTextField.getText());
        eleve.setPrenomEleve(prenomeleveTextField.getText());
        eleve.setDatedenaissance(eleveDateChooser.getDate());
        if (masculinRadioButton.isSelected()) {
            eleve.setSexe("homme");
        } else {
            eleve.setSexe("femme");
        }

        eleve.setAdresseEleve(adresseTextField.getText());
        try {
            eleve.setCdpostal(Integer.valueOf(cdpostalTextField.getText()));
        } catch (NumberFormatException e) {
            eleve.setCdpostal(0);
        }
        eleve.setVil(villeTextField.getText());
        eleve.setNomResponsable(nomresponsableTextField.getText());

        String telresponsable = telresponsableTextField.getText();
        telresponsable = telresponsable.replace("-", "");
        telresponsable = telresponsable.replace(".", "");
        telresponsable = telresponsable.replace("/", "");
        eleve.setTelResponsable(telresponsable);

        eleve.setEmailResponsable(emailresponsableTextField.getText());
        return eleve;
    }

