     public Amendes getAmendesFromFields() {
        Amendes amende = new Amendes();
        amende.setIdamendes(0);
        amende.setNumeropv(numpvTextField.getText());
        try {
            amende.setMontantpv(Integer.valueOf(montantpvTextField.getText()));
        } catch (NumberFormatException e) {
            amende.setMontantpv(0);
        }
        amende.setDatepv(datepv.getDate());

       return amende; 
    }


